#Note: In Python, Increment/Decrement Operator is not supported

#Then, how can we achieve this facility of incrementing and decrementing the variable value?

#Solution: using + and - opeartor.

num = 10

#Example to understand Decrement

#decrease the value of num by 1
num              =   num - 1
#left(variable)  =   right(value)
print(num) #9

#decrease the value of num by 7
num = num - 7
print(num) #2


#Example of Increment

#Increase the value of num by 1

num = num + 1
print(num) #3

#Increase the value of num by 10

num = num + 10

print(num) #13


#Shorthand way of defining/using arithmetic opeartor:

#Increase the value of num by 2

num = num + 2 #normal way(long way)

#shorthand way:

num+= 2



#Other Operators


num = num + 1 # shorthand way: num+=1
num = num - 1 # shorthand way: num-=1
num = num * 1 # shorthand way: num*=1
num = num / 1 # shorthand way: num/=1
num = num // 1 # shorthand way: num//=1
num = num ** 1 # shorthand way: num**=1
